package reforco;

public class loopWhile {

	public static void main(String[] args) {
		// Reforço de Lçao de Repetição
		
		int i= 1;
		int max = 10;
		System.out.println("Contando até " + max);
		while(i < max) {
			System.out.println("Valor de i: " + i);
			i++;
			
			
			
		}
	System.out.println(i);
	}
	
	

}
